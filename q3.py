#Faça um Programa que converta metros para centímetros

print("--------Conversor de unidade---------")
valor_M = float(input("Digite o valor em metros: "))
print("**********************************")
print("Valor sendo convertido em centímetros......")
valor_C = float(valor_M * 100)
print(".....Conversão completada")
print(f"O valor {valor_M}m, é {valor_C}cm")
